package java11;
public class Java11 {
    public static void main(String[] args) {
        System.out.println("Ваш вес на земле будет ");
        double we = 40.2;
        System.out.println(we);
        
        System.out.println("Ваш вес меркурие будет ");
        double se = we * .378;
        System.out.println(se);
        
        System.out.println("Ваш вес на луна будет ");
        double ne = we * .116;
        System.out.println(ne);
        
        System.out.println("Ваш вес на юпитере будет ");
        double re = we * 2.364;
        System.out.println(re);
        
    }
    
}
